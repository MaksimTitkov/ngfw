def get_lang(request) -> str:
    return request.cookies.get("lang", "ru")


def make_t(lang: str):
    tr = TRANSLATIONS.get(lang, TRANSLATIONS["ru"])
    en = TRANSLATIONS["en"]

    def t(key: str, *args) -> str:
        text = tr.get(key) or en.get(key) or key
        if args:
            try:
                return text.format(*args)
            except (IndexError, KeyError):
                return text
        return text

    return t


def base_ctx(request, **kwargs) -> dict:
    from app.version import VERSION, RELEASE_CHANNEL
    lang = get_lang(request)
    return {
        "request": request,
        "lang": lang,
        "t": make_t(lang),
        "app_version": VERSION,
        "app_channel": RELEASE_CHANNEL,
        **kwargs,
    }
